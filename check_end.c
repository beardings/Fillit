#include "libft.h"

int		check_end(t_block block, int size)
{
	int i;

	i = 0;
	while (i < 4)
	{
		if (block.x[i] == size - 1 && block.y[i] == size - 1)
			return (1);
		i++;
	}
	return (0);
}
